function showDialog() {
    var dialog = document.getElementById("Dialog");
    var filter= document.getElementById("filter");
    dialog.classList.toggle("show");
    filter.classList.toggle("show");
}
function closeDialog() {
    var dialog = document.getElementById("Dialog");
    var filter = document.getElementById("filter");
    dialog.classList.toggle("show");
    filter.classList.toggle("show");
}
